// Елементи
const img = document.querySelector(".content img");
const popup = document.getElementById("blessing-popup");
const blessingText = document.getElementById("blessing-text");
const closeBtn = document.querySelector(".close-btn");

// Клік по картинці → показати попап
img.addEventListener("click", () => {
  const random = Math.floor(Math.random() * blessings.length);
  blessingText.textContent = blessings[random];
  popup.style.display = "flex";
});

// Закриття кнопкою
closeBtn.addEventListener("click", () => {
  popup.style.display = "none";
});

// Закриття кліком поза вікном
popup.addEventListener("click", (e) => {
  if (e.target === popup) popup.style.display = "none";
});
