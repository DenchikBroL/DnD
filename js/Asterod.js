// Елементи попапу (якщо структура така сама)
const starImg = document.querySelector(".content img");
const starPopup = document.getElementById("blessing-popup");
const starText = document.getElementById("blessing-text");
const starClose = document.querySelector(".close-btn");

// Клік по картинці → випадкове благословення
starImg.addEventListener("click", () => {
  const random = Math.floor(Math.random() * starBlessings.length);
  starText.textContent = starBlessings[random];
  starPopup.style.display = "flex";
});

// Закриття
starClose.addEventListener("click", () => {
  starPopup.style.display = "none";
});

starPopup.addEventListener("click", (e) => {
  if (e.target === starPopup) starPopup.style.display = "none";
});
