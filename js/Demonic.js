// Елементи попапу (якщо структура така сама)
const voidImg = document.querySelector(".content img");
const voidPopup = document.getElementById("blessing-popup");
const voidText = document.getElementById("blessing-text");
const voidClose = document.querySelector(".close-btn");

// Клік по картинці → випадкове благословення
voidImg.addEventListener("click", () => {
  const random = Math.floor(Math.random() * voidBlessings.length);
  voidText.textContent = voidBlessings[random];
  voidPopup.style.display = "flex";
});

// Закриття
voidClose.addEventListener("click", () => {
  voidPopup.style.display = "none";
});

voidPopup.addEventListener("click", (e) => {
  if (e.target === voidPopup) voidPopup.style.display = "none";
});
